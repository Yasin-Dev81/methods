"""
- meshgrid
    - چندتا آرایه تک بعدی میگیره و ترکیب میکنه و یه ماتریس برمیگردونه
    - هر تعداد آرایه ارسال کنیم همونقدر دریافت میکنیم
    - ایتم‌ها رو اونقدر تکرار میکنه تا آرایه‌ها همسایز بشن

"""
import numpy as np


a = np.array([1, 3, 2])
b = np.array([2, 9, 5])

x, y = np.meshgrid(a, b)

print(x, y, sep="\n----------------------------\n")
# اولی عمودی تکرار شده
# دومی افقی تکرار شده
