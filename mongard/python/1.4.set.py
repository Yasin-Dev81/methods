"""
- ویژگی‌های set:
    * بدون نظم هستن
    * شامل عناصر منحصربه‌فرد هستن
    * قابل تغییر هستن

- عملیات‌های set:
    * a - b
        ~ تفاضل عناصر
    * a | b
        ~ اجتماع
    * a & b
        ~ اشتراک
    * a ^ b
        ~ اجتماع - اشترک

"""

x = set(
    (2, 3.42, 'hello')
)

y = {2, 3.42, 'hello'}

z = set('hello')
z.add('0')
z.update('1', '23')

print(
    x,
    y,
    z
)
