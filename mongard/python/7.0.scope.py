"""
- scope
    * محدوده‌هایی که کد دسترسی به متغیر داره

- انواع محدوده‌ها:
    * global scope
        ~ در هرجایی از کد میشه از متغیرهاش استفاده کرد
    * local scope
        ~ فقظ تو همون محدوده از کد میشه از متغیرهاش استفاده کرد

"""
# تغییر یه متغیر گلوبال در محدوده لوکال
a = 10 

def show():
    global a
    a = 20
    print(a)

print(a)
show()
print(a)
